#you can create a dictionary using the dict keyword
#dictionaries are mutable, unordered collections of key-value pairs
first_dict = dict([('sape', 4139), ('guido', 4127), ('jack', 4098)])
print(first_dict) #output: {'sape': 4139, 'guido': 4127, 'jack': 4098}

#SECOND CASE
second_dict = dict(sape=4139, guido=4127, jack=4098)
print(second_dict) #output: {'sape': 4139, 'guido': 4127, 'jack': 4098}